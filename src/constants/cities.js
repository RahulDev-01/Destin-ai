/**
 * Normalizes a city name for URL lookup
 */
export const slugifyCity = (cityName) => {
  if (!cityName) return '';
  return cityName.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)+/g, '');
};

/**
 * Returns the theme for a given slug. Falls back to a deterministically generated theme based on the city name hash.
 */
export const getCityTheme = (slug) => {
  if (CITIES[slug]) return CITIES[slug];

  // Procedurally generate a theme based on the hash of the slug
  let hash = 0;
  for (let i = 0; i < slug.length; i++) {
    hash = slug.charCodeAt(i) + ((hash << 5) - hash);
  }
  hash = Math.abs(hash);

  const color1 = PALETTE[hash % PALETTE.length];
  const color2 = PALETTE[(hash + 5) % PALETTE.length];
  const color3 = PALETTE[(hash + 11) % PALETTE.length];
  const emoji = EMOJIS[hash % EMOJIS.length];

  const formattedName = slug.split('-').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join(' ');
  const reviewsCount = `${((hash % 90) + 10).toLocaleString()}k+`;
  const ratingValue = (4.0 + (hash % 10) / 10).toFixed(1);

  return {
    name: formattedName,
    description: 'Your next great adventure',
    emoji: emoji,
    gradientText: `from-${color1}-500 via-${color2}-500 to-${color3}-500`,
    bgClasses: `bg-${color1}-50`,
    bgBlob1: `bg-${color1}-300`,
    bgBlob2: `bg-${color2}-300`,
    bgBlob3: `bg-${color3}-300`,
    btnPrimary: `bg-gradient-to-r from-${color1}-600 to-${color2}-600 hover:from-${color1}-700 hover:to-${color2}-700 shadow-${color1}-500/50`,
    btnOutline: `border-${color1}-200 hover:border-${color1}-600 hover:bg-${color1}-50 text-${color1}-700`,
    themeColor: color1,
    defaultImage: 'https://images.unsplash.com/photo-1436491865332-7a61a109cc05?w=1600&q=80',
    reviews: reviewsCount,
    rating: ratingValue
  };
};
